#include "philo.h"

int	parse_arg(int ac, char **av)
{
	int	i;

	i = 0;
	if (ac < 5 || ac > 6)
		return (0);
	while (av[++i])
	{
		if (!atoi(av[i]))
			return (0);
	}
}
