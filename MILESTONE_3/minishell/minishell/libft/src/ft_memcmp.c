#include "../libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*str1;
	unsigned char	*str2;
	size_t			i;
	int				result;

	i = 0;
	result = 0;
	str2 = (unsigned char *)s2;
	str1 = (unsigned char *)s1;
	while (result == 0 && i < n && (str1 || str2))
	{
		if (str1[i] != str2[i])
			result = str1[i] - str2[i];
		i++;
	}
	return (result);
}
