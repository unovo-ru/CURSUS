#include "../libft.h"

void	ft_double_putstr_fd(char **s, int fd)
{
	int	i;
	int	j;

	i = 0;
	while (s[i])
	{
		j = 0;
		while (s[i][j])
		{
			write(fd, &s[i][j], 1);
			j++;
		}
		write(fd, "\n", 1);
		i++;
	}
}
