#include "../libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*spaces;

	spaces = (void *)malloc(nmemb * size);
	if (!spaces)
		return (NULL);
	if (nmemb == 0 || size == 0)
		return (spaces);
	ft_bzero(spaces, nmemb * size);
	return (spaces);
}
