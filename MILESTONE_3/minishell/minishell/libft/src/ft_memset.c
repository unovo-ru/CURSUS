#include "../libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*e;

	e = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		e[i] = (unsigned char)c;
		i++;
	}
	s = (void *)e;
	return (s);
}
