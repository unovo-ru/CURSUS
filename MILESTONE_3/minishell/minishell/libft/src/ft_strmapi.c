#include "../libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*new_string;
	unsigned int	i;

	i = 0;
	new_string = ft_strdup(s);
	if (!new_string)
		return (NULL);
	while (new_string[i])
	{
		new_string[i] = f(i, new_string[i]);
		i++;
	}
	return (new_string);
}
